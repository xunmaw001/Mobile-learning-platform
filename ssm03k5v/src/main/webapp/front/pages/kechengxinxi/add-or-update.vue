																jiaoshi: '',
												},
																				banjiOptions: [],
				banjiIndex: 0,
																xingqiOptions: [],
				xingqiIndex: 0,
																kejieOptions: [],
				kejieIndex: 0,
																kechengOptions: [],
				kechengIndex: 0,
																gonghaoOptions: [],
				gonghaoIndex: 0,
																				// 登陆用户信息
				user: {},
                                ro:{
                                   biaoti : false,
                                   banji : false,
                                   xingqi : false,
                                   kejie : false,
                                   kecheng : false,
                                   gonghao : false,
                                   jiaoshi : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																
																		// 下拉框
			res = await this.$api.option(`banji`,`banjimingcheng`,{});
			this.banjiOptions = res.data;
												// 自定义下拉框值
			this.xingqiOptions = "星期一,星期二,星期三,星期四,星期五,星期六,星期天".split(',')
