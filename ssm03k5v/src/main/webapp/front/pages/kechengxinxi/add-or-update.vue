												// 下拉框
			res = await this.$api.option(`kechengfenlei`,`kecheng`,{});
			this.kechengOptions = res.data;
												// 下2
			res = await this.$api.option(`jiaoshi`,`gonghao`,{});
			this.gonghaoOptions = res.data;
															
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`kechengxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='biaoti'){
					this.ruleForm.biaoti = obj[o];
					this.ro.biaoti = true;
					continue;
					}
					if(o=='banji'){
					this.ruleForm.banji = obj[o];
					this.ro.banji = true;
					continue;
					}
					if(o=='xingqi'){
					this.ruleForm.xingqi = obj[o];
					this.ro.xingqi = true;
					continue;
					}
					if(o=='kejie'){
					this.ruleForm.kejie = obj[o];
					this.ro.kejie = true;
					continue;
					}
					if(o=='kecheng'){
					this.ruleForm.kecheng = obj[o];
					this.ro.kecheng = true;
					continue;
					}
					if(o=='gonghao'){
					this.ruleForm.gonghao = obj[o];
					this.ro.gonghao = true;
					continue;
					}
					if(o=='jiaoshi'){
					this.ruleForm.jiaoshi = obj[o];
					this.ro.jiaoshi = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																										// 下二随
			async gonghaoChange (e) {
				this.gonghaoIndex = e.target.value
				this.ruleForm.gonghao = this.gonghaoOptions[this.gonghaoIndex]
				let res = await this.$api.follow(`jiaoshi`, `gonghao`,{
					columnValue: this.ruleForm.gonghao
				});
																																																												if(res.data.jiaoshi){
					this.ruleForm.jiaoshi = res.data.jiaoshi
				}
											},
												
			// 多级联动参数
																																																
																																													
																																													
															// 下拉变化
			banjiChange(e) {
				this.banjiIndex = e.target.value
				this.ruleForm.banji = this.banjiOptions[this.banjiIndex]
			},
												// 下拉变化
			xingqiChange(e) {
				this.xingqiIndex = e.target.value
				this.ruleForm.xingqi = this.xingqiOptions[this.xingqiIndex]
			},
												// 下拉变化
			kejieChange(e) {
				this.kejieIndex = e.target.value
				this.ruleForm.kejie = this.kejieOptions[this.kejieIndex]
			},
												// 下拉变化
			kechengChange(e) {
				this.kechengIndex = e.target.value
				this.ruleForm.kecheng = this.kechengOptions[this.kechengIndex]
			},
																		
																																																																		
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																				if(this.ruleForm.id){
					await this.$api.update(`kechengxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`kechengxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

